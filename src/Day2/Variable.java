package Day2;

public class Variable {

	public static void main(String[] args) 
	{
	//	primitive data types // byte , short . int , long , float , double , boolean and char
		
		byte b =-127;          
		short s=100;
		int x=100;
		long lg=9860994463l;        // either you can capital l and L
		float c=1000.345f; 			// either you can capital f and F
		double d=1000.45678D;		// either you can capital d and D 
		//double number = 9860994463.00d;
		char ch ='A';
		boolean b1 =false;
		
		System.out.println("Number belongs to integer :"+x);
		System.out.println("Number belongs to byte :"+b);
		System.out.println("Number belongs to short :"+s);
		System.out.println("Number belongs to double :"+d);
		System.out.println("Character :"+ch);
		System.out.println("Number belongs to Long :"+lg);
		System.out.println("Number belongs to boolean :"+b1);
		System.out.println("Number belongs to float:"+c);
		
	}

}
